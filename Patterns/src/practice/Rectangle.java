package practice;

public class Rectangle {

	public static void main(String[] args) {
		// Rectangle rule -> no. of rows != no. of column
		for(int i=1;i<=5;i++)     // for rows
		{
			for(int j=1;j<=10;j++) {      // for column
				System.out.print("* ");
			}
			System.out.println();
		}

	}

}
